public class NumPosition {
    public static void main(String args[]) {
        int num[] = {34, 67, 81, 87, 45, 54, 90, 83};
        int givenNum = 40;
        for(int p= 0; p < num.length; p++){
            if (givenNum == num[p])
                System.out.println("given number position : "+ p);
            }
        }
    }
